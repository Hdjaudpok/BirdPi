"""Phase A4: Prepare YOLO dataset with segment-based splits.

Organizes images and labels into train/val/test splits based on
video segments (NOT random) to prevent temporal data leakage.

Split strategy:
  - Train: segments 0-15 (~70%)
  - Val:   segments 16-19 (~17%)
  - Test:  segments 20-22 (~13%)

Generates data.yaml for YOLOv8 training.
"""

import json
import os
import shutil
import sys
from collections import Counter, defaultdict
from pathlib import Path

# --- Paths ---
DATASET_DIR = Path(__file__).resolve().parent / "dataset"
IMAGES_DIR = DATASET_DIR / "images"
LABELS_DIR = DATASET_DIR / "labels"
REVIEW_PATH = DATASET_DIR / "review_status.json"

# --- Split config ---
TRAIN_SEGMENTS = set(range(0, 16))   # 0-15
VAL_SEGMENTS = set(range(16, 20))    # 16-19
TEST_SEGMENTS = set(range(20, 23))   # 20-22

CLASS_NAMES = ["bird", "other_animal"]


def parse_segment_from_filename(filename: str) -> int:
    """Extract segment number from filename like seg003_001234_bird_entry.jpg"""
    # Format: seg{NNN}_{NNNNNN}_{label}.jpg
    parts = filename.split("_")
    seg_str = parts[0].replace("seg", "")
    return int(seg_str)


def load_review_status() -> dict:
    """Load manual review status if available."""
    if REVIEW_PATH.exists():
        with open(REVIEW_PATH, encoding="utf-8") as f:
            return json.load(f)
    return {}


def main():
    print("[A4] Prepare YOLO dataset (segment-based splits)")

    images = sorted(IMAGES_DIR.glob("*.jpg"))
    if not images:
        print("[ERR] No images in", IMAGES_DIR)
        sys.exit(1)

    review = load_review_status()
    rejected = {k for k, v in review.items() if v == "rejected"}
    print(f"  Total images: {len(images)}")
    print(f"  Rejected (will skip): {len(rejected)}")

    # Create split directories
    for split in ["train", "val", "test"]:
        (DATASET_DIR / split / "images").mkdir(parents=True, exist_ok=True)
        (DATASET_DIR / split / "labels").mkdir(parents=True, exist_ok=True)

    stats = defaultdict(lambda: {"images": 0, "with_labels": 0, "bird": 0, "other": 0})

    for img_path in images:
        filename = img_path.name

        # Skip rejected images
        if filename in rejected:
            continue

        seg = parse_segment_from_filename(filename)

        if seg in TRAIN_SEGMENTS:
            split = "train"
        elif seg in VAL_SEGMENTS:
            split = "val"
        elif seg in TEST_SEGMENTS:
            split = "test"
        else:
            print(f"  [WARN] Unknown segment {seg} for {filename}, skipping")
            continue

        # Copy image
        dst_img = DATASET_DIR / split / "images" / filename
        shutil.copy2(str(img_path), str(dst_img))

        # Copy label
        label_name = img_path.stem + ".txt"
        src_label = LABELS_DIR / label_name
        dst_label = DATASET_DIR / split / "labels" / label_name

        has_labels = False
        if src_label.exists():
            content = src_label.read_text().strip()
            if content:
                shutil.copy2(str(src_label), str(dst_label))
                has_labels = True
                # Count classes
                for line in content.split("\n"):
                    cls_id = int(line.split()[0])
                    if cls_id == 0:
                        stats[split]["bird"] += 1
                    else:
                        stats[split]["other"] += 1

        if not has_labels:
            # Empty label file = negative sample (no objects)
            dst_label.write_text("")

        stats[split]["images"] += 1
        if has_labels:
            stats[split]["with_labels"] += 1

    # Print stats
    print(f"\n  {'Split':<8} {'Images':>7} {'With dets':>10} {'Birds':>7} {'Other':>7}")
    print(f"  {'-'*42}")
    for split in ["train", "val", "test"]:
        s = stats[split]
        print(f"  {split:<8} {s['images']:>7} {s['with_labels']:>10} {s['bird']:>7} {s['other']:>7}")

    total_imgs = sum(s["images"] for s in stats.values())
    print(f"  {'total':<8} {total_imgs:>7}")

    # Generate data.yaml
    yaml_path = DATASET_DIR / "data.yaml"
    yaml_content = f"""# BirdPi YOLO Dataset
# Auto-generated by prepare_yolo_dataset.py
# Split: segment-based (no temporal leakage)

path: {DATASET_DIR.as_posix()}
train: train/images
val: val/images
test: test/images

nc: {len(CLASS_NAMES)}
names: {CLASS_NAMES}
"""
    yaml_path.write_text(yaml_content)
    print(f"\n  data.yaml: {yaml_path}")

    # Summary JSON
    summary = {
        "splits": {k: dict(v) for k, v in stats.items()},
        "train_segments": sorted(TRAIN_SEGMENTS),
        "val_segments": sorted(VAL_SEGMENTS),
        "test_segments": sorted(TEST_SEGMENTS),
        "rejected_count": len(rejected),
        "classes": CLASS_NAMES,
    }
    summary_path = DATASET_DIR / "dataset_summary.json"
    with open(summary_path, "w", encoding="utf-8") as f:
        json.dump(summary, f, indent=2)
    print(f"  Summary: {summary_path}")

    print("\n[DONE] Dataset ready for YOLOv8 training")


if __name__ == "__main__":
    main()
